package com.may.bean;

public class Help
{
	private int id;

	private String mom;

	private String dad;

	private int age;

	public int getId()
	{
		return id;
	}

	public void setId(int id)
	{
		this.id = id;
	}

	public String getMom()
	{
		return mom;
	}

	public void setMom(String mom)
	{
		this.mom = mom;
	}

	public String getDad()
	{
		return dad;
	}

	public void setDad(String dad)
	{
		this.dad = dad;
	}

	public int getAge()
	{
		return age;
	}

	public void setAge(int age)
	{
		this.age = age;
	}

	

}
